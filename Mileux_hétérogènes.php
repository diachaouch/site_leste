									<p class="mt-3">Certains métaux (Mg, V, Pd,…) ou composés intermétalliques sont connus pour leurs capacités à absorber de façon réversible de grandes quantités d'hydrogène. Les composés ainsi formés sont appelés hydrures métalliques.<br>

Les physiciens et les chimistes n'ont jamais cessé d'accorder un intérêt accru aux hydrures métalliques comme une classe assez intéressante de matériaux. Cet intérêt s'est manifestée bel et bien après la découverte de leur application comme modérateur dans les réacteurs nucléaires ou aussi après la découverte des propriétés de sorption étonnantes de l'hydrogène par les composés intermétalliques (à savoir chaleur de réaction, capacité de stockage, rapidité du cycle d'absorption - désorption, etc…). Grâce à ces propriétés thermodynamiques, les hydrures ont été utilisés pour développer un grand nombre d'applications industrielles propres et silencieuses telles que les piles à combustible, les unités de stockage de l'énergie thermique et électrique, le stockage de l'hydrogène, les pompes à chaleur, les moteurs thermiques, et les batteries électriques.<br>

Si les promesses  de ces différentes applications industrielles sont incontestables, elles reposent cependant sur deux paris : celui de la compétitivité et bien sûr celui de la sûreté, sûreté d’autant plus mise en avant que le gaz envisagé soit l’hydrogène et que ces applications concerneront la plupart des consommateurs que nous sommes, qu’il s’agisse de l’alimentation en électricité des véhicules, des maisons, des ordinateurs ou des téléphones portables ou la génération des hautes et des basses températures. En effet, si l’hydrogène est un combustible propre qui génère beaucoup moins de polluants chimiques qu’un moteur à explosion classique par exemple, c’est aussi un gaz potentiellement dangereux qui peut s’enflammer ou exploser en présence d’air si certaines conditions  sont réunies.<br>

Les réacteurs métal-hydrogène constituent des éléments importants de plusieurs types d'installations industrielles. Les qualités premières de ces réacteurs sont d'abord d'assurer des conditions d'application de la température et de la pression à la masse de poudre occluse, en y optimisant les transferts de chaleur de façon homogène et rapide. Comme la formation des hydrures est exothermique (décharge = endothermique), il est nécessaire de bien savoir piloter ces transferts pour la meilleure dynamique du système. Donc une forte activité de simulation numérique et de calculs est indispensable, d’une part, pour guider la définition des caractéristiques géométriques et le choix des matériaux d'emballage et d'échange, d’autre part, pour le pilotage optimisé du réacteur (apport de calories/frigories, cinétique d'extraction/insertion de H2 sous pression, contrôle de la pression, contrôle de l'endothermicité/exothermicité…). Par conséquent, plusieurs investigations expérimentales et numériques, décrivant ces mécanismes de transferts complexes, ont été effectuées.<br>

A l’échelle nationale, on trouve essentiellement deux équipes (au Laboratoire d’Etudes des Systèmes Thermiques et Energétiques à l’Ecole Nationale d’Ingénieurs de Monastir et au Laboratoire des matériaux et de procédés à l’Ecole Supérieure Scientifique et Technique de Tunis) qui travaillent sur les hydrures métalliques. La première s’intéresse  aux transferts de chaleur et de masse dans les réacteurs métal - hydrogène, à la caractérisation des hydrures et leurs applications dans le domaine énergétique. La seconde s’occupe du phénomène physico chimique des hydrures. <br>
A l’échelle internationale, compte tenu du grand intérêt accordé aux hydrures métalliques et leurs applications, plusieurs congrès internationaux qui ont un rapport avec les hydrures métalliques sont organisés. On peut citer : l’International Symposium on Metal Hydride Systems, Grove Fuel Cell Symposium, European Hydrogen Energy Conferences.<br></p>
									<p>
									A l’issue  d’une étude bibliographique, il s’est avéré que les travaux existants sont approchés et sont basés sur les hypothèses simplificatrices suivantes :

 <ul><li>les transferts sont monodimensionnels.</li>
	 <li>les températures du solide (métal) et du gaz (hydrogène) sont égales (ETL).</li>
	 <li>la pression du gaz est constante.</li>
	 <li>l’écoulement est régi par la loi de Darcy.</li>
	 <li>l’écoulement et les transferts dans la zone d’expansion du réacteur sont négligeables.</li>
	 <li>les transferts radiatifs sont négligeables.</li>
	 <li>les réacteurs simulés sont de formes géométriques simples.</li>
</ul>


Ces hypothèses conduisent à des modèles et des codes numériques très restrictifs. D’où la nécessité d’une étude rigoureuse qui permettra, d’une part, de mieux comprendre les phénomènes et de qualifier les incidences de ces hypothèses et d’autre part, d’établir des modèles assez généraux et des outils numériques souples et efficaces.
Jusqu’à l’heure actuelle (fin 2003), les travaux effectués au sein du LESTE ont permis l’élaboration d’un modèle qui tient compte des effets bidimensionnels, de la résistance à l’écoulement, de la différence entre la température du solide et du fluide, des transferts radiatifs et de l’écoulement et des transferts dans la zone d’expansion du réacteur. Un outil informatique est mis en place pour la simulation du fonctionnement du réacteur en utilisant ce modèle.
Ainsi, ces investigations ont permis d’étendre l’étude numérique à des configurations plus réalistes des réacteurs métal-hydrogène.<br>

Le modèle théorique fait intervenir des paramètres dont les plus influents (suite à une étude de sensibilité que nous avons effectuée) sont : la pression d’équilibre, la cinétique d'extraction/insertion de H2 sous pression et la conductivité thermique.<br> Alors  un banc de mesure (réacteur métal-hydrogène, réservoir d’hydrogène, instruments de mesure…) a été réalisé afin de déterminer ces propriétés. <br>Les mesures expérimentales nous ont permis de proposer des modèles analytiques pour la pression d’équilibre et de la cinétique de la réaction d’hydruration/déshydruration
									</p>
									<h4>Objectifs du programme de recherche: </h4>
								On se propose dans ce programme de développer des logiciels de simulation numérique assez généraux pour les systèmes métal-hydrogène et de mettre en place des outils expérimentaux permettant la validation des logiciels et la caractérisation d’une large variété de métaux et de composés intermétalliques. Ces outils numériques et expérimentaux vont nous permettre :

									<ul>
