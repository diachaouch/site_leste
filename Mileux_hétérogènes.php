									<p class="mt-3">Le séchage est une opération fréquemment rencontrée dans plusieurs secteurs industriels (matériaux de construction, agro-alimentaire, textile, produits pharmaceutiques, ….). C’est une opération qui consomme beaucoup d’énergie et qui peut engendrer lorsqu’elle est mal conduite une altération de la qualité du produit séché  (déformation, fissuration, oxydation, …). Depuis plusieurs années, beaucoup de chercheurs s’intéressent aux phénomènes de transferts de chaleur et de masse qui ont lieu au cours du séchage et ceci dans le but de réduire le coût sans affecter la qualité du produit  fini.<br> Malgré l’abondance des publications sur le séchage, il ressort de l’étude bibliographique que :
                                   <ul><li>les études sur le séchage en présence d’un soluté dans le liquide sont rares et incomplètes.</li>
									<li>il y a très peu d’études sur le séchage qui tiennent compte d’une façon rigoureuse du couplage entre les transferts dans le milieu fluide et dans les milieux poreux. En général les transferts dans le milieu fluide sont approchés par des coefficients d’échange constants au cours du séchage.</li>
									<li>le séchage des matériaux présentant une double porosité (milieux granulaires, milieux fibreux, textiles, papiers, ….) n’a pas fait l’objet de suffisamment d’études.</li></ul>
                                    Au laboratoire, nous avons développé des modèles qui régissent les transferts dans les milieux poreux et dans le fluide sécheur. Des codes de calcul ont été mis en place. Ces codes nous ont permis de mener les études suivantes: 
									<ol>
									<li>Une première étude dans laquelle nous avons analysé d’une manière détaillée le phénomène de l’évaporation de l’eau dans un écoulement bidimensionnel d’air sec, d’air humide et de vapeur surchauffée. L’étude a été réalisée principalement sur le domaine de validité de l'analogie entre le transfert de chaleur et de masse et sur la température d'inversion. Cette contribution a conduit aux principales conclusions suivantes :
										<ul>
										<li>A haute température  et en présence de rayonnement, la vitesse interfaciale affecte les différents profils.</li>
										<li> La température d'inversion augmente avec  la vitesse et la pression ambiante et diminue avec le chauffage.</li>
										<li>Le transfert de chaleur par rayonnement fait diminuer la température d’inversion.</li>
										<li>L’analogie entre le transfert de chaleur et le transfert de masse est valable uniquement pour les faibles concentrations et les faibles températures ambiantes; en présence du transfert de chaleur par rayonnement, l’analogie n’est pas valable.</li>
										</ul>
										</li>
									<li>Une  deuxième étude au cours de laquelle nous avons analysé, à l’échelle macroscopique, les transferts bi-dimensionnels de chaleur et de masse au cours du séchage convectif d’une plaque poreuse non saturée, composée d’une matrice solide inerte et indéformable, d’une phase liquide (eau pure) et d’une phase gazeuse (air + vapeur d’eau). Les transferts dans les milieux fluides sont représentés par des coefficients d’échanges obtenus à partir de l’étude précédente.<br>
									Les simulations tiennent compte de la pression gazeuse et montrent l’effet des conditions externes et initiales sur les variables d’état (température du milieu, pression gazeuse et saturation en eau). Elles montrent aussi l’effet des variations des coefficients locaux des transferts de chaleur et de masse, obtenus sous plusieurs conditions, sur l’opération de séchage.</li>
									<li>Une troisième étude, à l’échelle macroscopique aussi, ou nous nous sommes intéressé au séchage des milieux granulaires par convection naturelle, forcée et mixte. Les grains sont des milieux poreux et le milieu granulaire est un milieu à double porosité. Nous avons supposé que les grains sont indéformables et qu’il n’y a pas de phase liquide dans les macro pores. Les effets des paramètres internes et externes sur les transferts de chaleur et de masse par convection naturelle et forcée sont présentés et analysés. L’étude dans le cas de la  convection naturelle tient compte du  transfert radiatif.<br>
									Lors de l’étude des transferts de chaleur et de masse au cours du séchage, nous nous sommes principalement heurtés au problème des forts gradients de teneur en liquide pendant l’apparition du front de séchage. Ce problème est plus marqué pour les hautes températures et concentrations ambiantes et allonge considérablement le temps de calcul et ne permet pas actuellement d'envisager de traiter d'une façon rigoureuse le couplage des transferts à l'intérieur du milieu poreux et dans le fluide extérieur et de faire des simulations pour des configuration de géométries complexes.</li>
									</ol></p>
                       
 

 
									<h4>Objectifs du programme de recherche: </h4>
									<ul>
									<li>Amélioration des codes numériques mis en place en les rendant plus rapides, plus robustes et utilisables facilement pour des géométries plus complexes. Les codes à notre disposition actuellement sont basés sur les méthodes des volumes finis en maillage structuré et de ce fait sont difficilement applicable pour des géométries complexes.</li>

                                    <li>Amélioration des modèles en tenant compte des différents modes de transferts (conductif, convectif et radiatif) et en tenant compte d'une façon plus rigoureuse du couplage entre les transferts dans le milieu poreux et dans le fluide environnant.</li>
									<li>Amélioration des outils expérimentaux (boucle de séchage, bancs de caractérisation, ….).</li>
									<li>Etude des transferts d’eau et de soluté au cours du séchage d’un milieu poreux.</li>
									<li>Etude du séchage des milieux à  double porosité (milieux granulaires, milieux fibreux, …).</li>
									</ul>
								<h4>Résultats attendus du programme de recherche:  </h4>
								<ul>
								<li>Mise en place d’outils d’étude théorique performants et peu restrictifs  pour l’étude des transferts de chaleur   et de masse au cours du séchage.</li>
								<li>Amélioration des outils expérimentaux (boucle de séchage, bancs de caractérisation, ….).</li>
								<li>Compréhension et maîtrise  des phénomènes de transferts de chaleur et de masse au cours du séchage des milieux poreux à double porosité.</li>
								<li>Compréhension et maîtrise  des phénomènes de cristallisation et  de transferts de chaleur et de masse au cours du séchage lorsque la phase liquide contient un soluté.</li>
								</ul>
									<h4>Méthodologie et approche envisagées pour la réalisation du programme de recherche: </h4>
                                     <p>Nous effectuerons des  comparaisons des performances des  solveurs (ORTHOMIN, GMRES, BIGRADIENT, …)  et des stratégies de résolution numérique (couplées, non couplées). Ceci nous permettra de choisir les solveurs et les stratégies les plus adaptés aux problèmes considérés. D’autre part nous comptons, afin d’avoir des codes de calcul qui peuvent s’adapter facilement à des géométries complexes, effectuer la discrétisation par les méthodes de volume de contrôle à base d’éléments finis utilisant un maillage non structuré.<br>
Afin de généraliser d’avantage les modèles nous introduirons l’effet du rayonnement et nous traiterons d’une façon plus rigoureuse le couplage entre les transferts dans le milieu poreux et ceux dans le milieu fluide environnant. Il s’agit de résoudre simultanément les équations de transport dans les deux milieux.
La boucle de séchage sera améliorée en éliminant les vibrations et en régulant avec plus de précision la température et l’humidité. Ainsi, cette boucle permettra de réaliser des essais de séchage qui serviront à déterminer la cinétique de séchage et à affiner et valider les modèles théoriques.<br>
 Le séchage lorsque l’eau est  chargée en sels sera étudié  essentiellement à l’échelle porale. Cette étude s’appuiera sur des expériences sur des capillaires et sur des réseaux gravés (micro-modèle), des simulations numériques sur réseau de  pores et en ce qui concerne certains aspects théoriques sur la théorie de la percolation (percolation d’invasion, percolation d’invasion sous gradient). <br>
Pour un capillaire de différentes géométries, on  tentera de répondre aux deux questions suivantes : quel est l’impact du sel sur les flux d’évaporation ?  Dans quelle région du capillaire ou du réseau  la cristallisation va-t-elle apparaître? <br> 
Des réponses à ces questions seront  obtenues en combinant études d’ordre de grandeurs, solutions numériques et / ou analytiques et visualisations expérimentales. 
Concernant le séchage des matériaux présentant une double porosité, nous généraliserons nos outils théoriques en tenant compte de la présence d’une phase liquide dans les macro pores.</p>

