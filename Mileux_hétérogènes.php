									<p class="mt-3">La formation d’une phase gazeuse, au sein d’un milieu poreux, par détente d’une solution liquide sursaturée en gaz ou par ébullition, constitue une transition du premier ordre, dans laquelle deux événements jouent un rôle décisif : l’apparition des bulles de gaz par nucléation, puis le développement de ces bulles dont le volume croit par expansion et diffusion pour ramener le système vers un nouvel état d’équilibre.
De point de vue pratique, ce phénomène joue un rôle  important dans de nombreux processus industriels (séchage, caloduc, pompe à bulles,  pétrole, …). En génie pétrolier où parfois, lors de la mise en production de certains gisements de pétrole, on se contente, dans une première étape, d’ouvrir les puits et de laisser les gisements se décomprimer. Dans ces conditions, le gaz initialement dissous dans l’huile est désorbé et la phase gazeuse, ainsi crée, déplace l’huile vers les puits de production. Cette pratique consistant à exploiter l’énergie potentielle  du gisement est appelée drainage naturel ou récupération  primaire.
L’étude des transferts de chaleur et de masse lors de la formation de la phase gazeuse a fait l’objet de nombreux travaux  théorique et expérimentaux. Deux approches sont utilisées. L’approche macroscopique ou les phénomènes sont décrits en moyennes sur un volume élémentaire représentatif, contenant plusieurs dizaines de pores. En général, pour les milieux poreux réel, les distributions géométriques des phases  sont inconnues et par conséquent  uniquement cette approche est applicable.
L’approche microscopique ou les phénomènes sont décrits à l’échelle du pore permet une analyse fine des phénomènes mais n’est applicable que dans le cas de milieux poreux pour lesquels les distributions géométriques des phases sont connues ( réseaux gravés, géométrie périodique, ….). 
Lors de l’ébullition ou de la formation des gaz, le milieu poreux sera éventuellement formé de zones saturées (gaz ou liquide) et de zones non saturées (gaz+liquide) séparées par des interfaces mobiles. De ce fait, une difficulté apparaît, lors de la résolution numérique des équations macroscopiques, au niveau de l’adaptation du maillage et sa modification au cours du temps. Certaines méthodes de résolution fixent l’interface en effectuant une transformation des coordonnées spatiales et temporelles. D'autres méthodes considèrent que le milieu poreux contient partout une phase liquide et une phase gazeuse. Le milieu poreux est alors partout non saturé. Les transferts sont régis par trois équations relatives à la température, à la pression de la phase gazeuse et à la saturation. 
La formulation enthalpique du problème est  aussi utilisée. Le milieu poreux est considéré comme étant une seule phase et les mêmes équations de transferts régissent le problème dans tout le milieu poreux.
Au Laboratoire, nous avons étudié, à l’échelle macroscopique, l’ébullition en milieux poreux en utilisant la formulation enthalpique (ou méthode de mélange). La méthode des volumes finis a été utilisée pour la discrétisation du système d’équations. Le modèle de cavité soumise à un flux de chaleur par le bas et refroidie par le haut, a été choisi pour valider  les résultats obtenus (stabilité, convection,...) à l’aide du code de calcul développé. Des résultats concernant l’étude de l’ébullition dans une couche poreuse chauffée sont obtenus. 
